"""
Позиции приборов на ФУНКЦИОНАЛЬНОЙ СХЕМЕ (ГОСТ 21.408/21.208).

Проверяется то, чего в отчёте не видно: позиция прибора - ключ, которым ФСА
сшивается с кабельным журналом (графа «№ кабеля» заполнена ровно позициями) и с
перечнем входных/выходных параметров. Промах в разборе не даёт ложной находки -
он даёт МОЛЧАНИЕ: сверка просто не находит, что сверять.

Фикстуры здесь нет и не нужно: find_instruments - чистая функция от списка
надписей с рамками, а сами надписи сняты с «24-051-АК».
"""

import pytest

from conftest import load_script

F = load_script("functional_scheme_to_data.py")


def span(text, x0, y0, x1=None, y1=None):
    """Надпись с рамкой; по умолчанию кегль 10 и ширина по числу знаков."""
    return {"text": text,
            "bbox": [x0, y0, x1 if x1 is not None else x0 + 6 * len(text),
                     y1 if y1 is not None else y0 + 10]}


def tags(spans, page=1):
    return sorted({i["tag"] for i in F.find_instruments(spans, page)})


def test_whole_tag_with_and_without_hyphen():
    """«FT-401» и «PG2007» - одна и та же форма записи, дефис необязателен."""
    assert tags([span("FT-401", 10, 10), span("PG2007", 100, 10)]) == ["FT401", "PG2007"]


def test_letters_over_digits_are_stitched():
    """Буквы над цифрами в одном кружке - одна позиция, а не две надписи.

    Так подписано 76 приборов на листе 31 «24-051-АК»: экспорт CAD разложил
    содержимое кружка на две надписи, и порознь они не значат ничего.
    """
    assert tags([span("MOV", 100, 100, 118, 110),
                 span("1201", 99, 111, 121, 121)]) == ["MOV1201"]


def test_digits_far_from_letters_are_not_stitched():
    """Цифры В СТОРОНЕ от букв - другой прибор или номер трубопровода.

    Допуск задан в высотах надписи, а не в пунктах: форматы листов альбома идут
    от A4 до 5054x2384, и допуск, подобранный на одном, промахивается на другом.
    """
    assert tags([span("MOV", 100, 100, 118, 110),
                 span("1201", 400, 111, 422, 121)]) == []


def test_pipe_diameter_is_not_an_instrument():
    """DN100 - условный диаметр трубопровода, а не прибор.

    По форме он неотличим от позиции, и на одном листе 27 их восемь разных.
    Список исключений намеренно короткий: длинный превратился бы в фильтр
    «оставить только то, что уже видели», и прибор нового бюро пропал бы молча.
    """
    assert tags([span("DN100", 10, 10), span("PN16", 60, 10),
                 span("PT206", 120, 10)]) == ["PT206"]


@pytest.mark.parametrize("text", ["Т10", "отбор", "4...20", "мА", "1:2.5"])
def test_plain_text_is_not_a_tag(text):
    """Обозначения трубопроводов, единицы и подписи позициями не являются."""
    assert tags([span(text, 10, 10)]) == []


def test_tag_is_reported_on_every_sheet_it_appears_on():
    """Один прибор встречается на нескольких листах - это норма, а не дубль."""
    found = F.find_instruments([span("PT206", 10, 10)], 3)
    assert found and found[0]["sheet"] == 3 and found[0]["split"] is False
