"""
Нарезка альбома: границы документов, тип части, обозначение шкафа.

Каждый случай здесь - это отказ, замеренный на реальном альбоме, а не выдумка.
Ошибки этого модуля почти все ТИХИЕ: часть уехала не в тот шкаф - сверка идёт
между документами разных щитов и даёт вал ложных «разный артикул»; шкаф не
опознан - документы одного щита разъехались по двум связкам и не сверились
вовсе, без единого сообщения.
"""

import pytest

import full_project as fp


# ---------------------------------------------------------------- границы

def test_empty_title_inherits_previous():
    """Лист без заполненной графы наименования - это форма 4, продолжение
    предыдущего документа, а не новый документ."""
    parts = fp.split_into_parts(["Схема ЩС1", "", "", "Общий вид ЩС1", ""])
    assert [(p["title"], p["first_page"], p["last_page"]) for p in parts] == [
        ("Схема ЩС1", 0, 2),
        ("Общий вид ЩС1", 3, 4),
    ]


def test_pages_before_first_title_are_not_a_document():
    """Обложка и содержание тома идут до первого наименования и документа
    не образуют."""
    parts = fp.split_into_parts(["", "", "Схема ЩС1"])
    assert len(parts) == 1 and parts[0]["first_page"] == 2


def test_same_title_after_gap_is_one_part():
    parts = fp.split_into_parts(["Схема ЩС1", "", "Схема ЩС1"])
    assert len(parts) == 1 and parts[0]["last_page"] == 2


# ---------------------------------------------------------------- тип части

@pytest.mark.parametrize("title,expected", [
    ("Схема электрическая принципиальная ЩС2", "scheme"),
    ("Схема электрическая однолинейная ЩС1", "scheme"),
    # Схема внешних соединений - ЧЕРТЁЖ с клеммниками, а не построчная таблица:
    # netlist_to_json достаёт из неё ровно ноль строк (проверено на 8 листах ЭОМ)
    ("Схема соединений внешних проводок", "scheme"),
    ("Схема подключения внешних проводок", "scheme"),
    # а вот это - настоящие таблицы
    ("Кабельный журнал", "netlist"),
    ("Перечень входных сигналов", "netlist"),
    ("Спецификация оборудования, изделий и материалов", "spec"),
    ("Общий вид ЩС1", "assembly"),
    # лист-продолжение общего вида: штамп чертёжной формы обрезает наименование
    ("Вид спереди", "assembly"),
])
def test_classify(title, expected):
    assert fp.classify(title)[0] == expected


@pytest.mark.parametrize("title", [
    "План расположения оборудования",
    "Пояснительная записка",
    "Молниезащита и заземление",
    "Установочный чертеж датчика",
])
def test_skipped_titles(title):
    """Комплектацию шкафа эти листы не описывают - сверять по ним нечего,
    и тащить их в отчёт значит только шуметь."""
    assert fp.classify(title)[0] is None


def test_external_connections_beats_generic_scheme():
    """Порядок в TITLE_TO_TYPE важен: «схема внешних соединений» обязана
    попасть в scheme раньше, чем сработает общее «схема ...»."""
    assert fp.classify("Схема соединений внешних проводок")[0] == "scheme"


# ---------------------------------------------------------------- шкаф

@pytest.mark.parametrize("title,expected", [
    ("Схема электрическая принципиальная ЩС2", "ЩС2"),
    ("Щит автоматики котла (ЩАК1). Общий вид", "ЩАК1"),
    ("Схема электрическая принципиальная ШУПЧ4", "ШУПЧ4"),
])
def test_detect_cabinet(title, expected):
    assert fp.detect_cabinet(title) == expected


def test_cabinet_stopwords():
    """Слова вида «СХЕМА»/«ВИД» по форме неотличимы от обозначения щита."""
    assert fp.detect_cabinet("Принципиальная схема") is None


def test_compound_object_code_is_not_a_cabinet():
    """В «ТТС-БМК-48000» средний сегмент по форме похож на щит, но это шифр
    всей котельной: спецификация с таким «шкафом» уехала бы в собственную
    связку вместо общей."""
    assert fp.detect_cabinet("Спецификация ТТС-БМК-48000") is None


def test_mixed_layout_is_unified():
    """Бюро пишет однолинейную схему как «ЩC1» с ЛАТИНСКОЙ C, а принципиальную
    того же шкафа - кириллицей. Без приведения это две разные связки, и два
    документа одного щита молча не сверятся друг с другом."""
    latin = fp.detect_cabinet("Схема электрическая однолинейная ЩC1")
    cyrillic = fp.detect_cabinet("Схема электрическая принципиальная ЩС1")
    assert latin == cyrillic == "ЩС1"


def test_pure_latin_token_not_mangled():
    """Чисто латинское обозначение - честная латиница, а не мешанина раскладок,
    и портить её нельзя."""
    assert fp._unify_layout("PLC") == "PLC"


def test_junk_lines_not_taken_for_title():
    """Графа «Формат» стоит в той же колонке, что наименование. Не выкинув её,
    каждый лист-продолжение получает собственное «наименование», и документ
    рассыпается полистно (замерено: 94 части вместо 48).

    ДВА ЗАМЕРЕННЫХ ПРОБЕЛА, которые решено НЕ ЗАКРЫВАТЬ. Значение масштаба
    «1:5» не ловится (двоеточия в числовой ветке нет), и метка, кончающаяся
    точкой, не ловится вместе со своим значением («Инв. №»): требование
    границы слова после точки выполняется, только если следом идёт буква.
    Оба случая проверены на обоих альбомах корпуса (184 и 237 листов) -
    штампы перечитаны с исправленной регуляркой, и наименования листов не
    изменились НИ ОДНО: части и их типы те же 9 и 48. До колонки наименования
    эти строки не доходят вовсе.

    Правка, которая на всём доступном корпусе не делает ничего, - это догадка
    «по форме, а не по замеру», от которой в этом модуле отказывались уже
    трижды (см. его шапку); риск же вполне реальный: на альбоме следующего
    бюро расширенный фильтр может выбросить настоящее наименование.
    """
    for junk in ("Формат", "А3", "A4x4", "Масштаб", "Лист", "1.5", "12/3"):
        assert fp.JUNK_LINE.match(junk), junk
    assert not fp.JUNK_LINE.match("Схема электрическая принципиальная")
