#!/usr/bin/env python3
"""
Скорость генерации сервера ИИ (токенов в секунду) за ПОСЛЕДНИЙ вызов - для
показа в интерфейсе сессии, рядом с отладкой.

ЗАЧЕМ И ПОЧЕМУ «СРЕДНЯЯ ПО ВЫЗОВУ», А НЕ ЖИВОЙ ПОТОК. В обычном чате токены
летят потоком и счётчик обновляется на лету (chat_llm.stream_reply). У стадии
ИИ анализа такого потока нет: агенты работают ВНУТРИ Open Interpreter (через
litellm), а наши прямые вызовы (зрение, мерджер, стадия отчёта) идут без stream.
Поэтому показываем среднюю по последнему ЗАВЕРШЁННОМУ обращению:
completion_tokens / время ответа. Это честная скорость модели, просто не живая.

УСТРОЙСТВО - ГЛОБАЛЬНЫЙ СТОК НА ПРОЦЕСС, как у llm_transcript: прогон исполняется
отдельным процессом на сессию, перепутать нечего. Куда именно уходит число,
модуль НЕ знает: эмиттер внедряется извне (main через progress.emit), поэтому
здесь нет ни stdout-маркера, ни знания про веб-интерфейс - только арифметика и
вызов колбэка. Не настроен - молчит (CLI и тесты не шумят).

Только стандартная библиотека: модуль тянет llm_client, который импортируют и
зрение, и мерджер, и проверка серверов.
"""

import threading

_lock = threading.Lock()
_emit = None


def configure(emit_fn) -> None:
    """Назначить приёмник статистики: emit_fn(**поля) -> None. None выключает."""
    global _emit
    with _lock:
        _emit = emit_fn


def is_active() -> bool:
    with _lock:
        return _emit is not None


def record(model=None, tokens=None, seconds=None) -> None:
    """Один завершённый вызов модели: сколько токенов ответа за сколько секунд.

    Нечего показать (нет токенов или времени) - молчим: пустой счётчик хуже
    отсутствующего. Ошибку эмиттера глотаем - счётчик не повод ронять прогон.
    """
    with _lock:
        emit = _emit
    if emit is None or not tokens or not seconds or seconds <= 0:
        return
    try:
        emit(tps=round(tokens / seconds, 1), tokens=int(tokens),
             seconds=round(seconds, 2), model=model)
    except Exception:  # noqa: BLE001 - счётчик не должен ронять прогон
        pass
