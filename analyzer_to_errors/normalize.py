"""
Приведение обозначений к сравнимому виду: омоглифы, ведущие нули, токены.

ГЛАВНОЕ, ЧТО ЗДЕСЬ ЕСТЬ, - ОДНА таблица омоглифов. Кириллические и латинские
буквы 'А', 'В', 'Е', 'К', 'М', 'Н', 'О', 'Р', 'С', 'Т', 'У', 'Х' на вид
неразличимы, а в проектной документации их мешают постоянно: 'ХТ01' кириллицей
и 'XT01' латиницей - одна и та же клеммная колодка, но разные строки.

Таблица была ДВАЖДЫ, в двух направлениях и в разных модулях:

  * bundle_rules сворачивал кириллицу в латиницу - ему нужен ключ сравнения, и
    направление безразлично, лишь бы одинаковое;
  * full_project сворачивал латиницу в кириллицу - обозначение шкафа становится
    именем папки и названием связки, которое читает человек, поэтому оно
    обязано остаться кириллическим ('ЩC1' с латинской C и 'ЩС1' - один щит).

Оба свёртывания нужны, и объединять их в одну функцию нельзя. Общей должна быть
ТАБЛИЦА: разойдись эти два списка на одну букву - и документы одного шкафа
разъедутся по двум связкам молча, без единого сообщения об ошибке. Ровно тот
сорт отказа, ради которого в bundles.py запрещено угадывать связку по имени
файла.
"""

import re

# Пары «кириллическая - латинская», неразличимые на вид. Единственный источник
# правды для обоих направлений свёртывания.
HOMOGLYPH_PAIRS = (
    ("А", "A"), ("В", "B"), ("Е", "E"), ("К", "K"), ("М", "M"), ("Н", "H"),
    ("О", "O"), ("Р", "P"), ("С", "C"), ("Т", "T"), ("У", "Y"), ("Х", "X"),
)

# Строчные пары: у части букв строчные начертания тоже совпадают, у части нет
# ('в' и 'b' различимы), поэтому список свой, а не lower() от верхнего.
HOMOGLYPH_PAIRS_LOWER = (
    ("а", "a"), ("е", "e"), ("о", "o"), ("р", "p"), ("с", "c"), ("у", "y"),
    ("х", "x"),
)

_ALL_PAIRS = HOMOGLYPH_PAIRS + HOMOGLYPH_PAIRS_LOWER

TO_LATIN = str.maketrans({cyr: lat for cyr, lat in _ALL_PAIRS})
TO_CYRILLIC = str.maketrans({lat: cyr for cyr, lat in HOMOGLYPH_PAIRS})

_SPACES_RE = re.compile(r"\s+")

# Ведущие нули в числовой группе. В ОБОЗНАЧЕНИЯХ они незначащие: спецификация
# пишет 'XA1', сборочный чертёж - 'XA001', и это одна и та же колодка. Без
# сворачивания сверка ША1/ИК давала 19 ложных «изделия нет на чертеже» из
# воздуха (XA1..XA19 против XA001..XA019).
_LEADING_ZERO_RE = re.compile(r"(?<![0-9])0+(?=[0-9])")


def fold(s):
    """Ключ сравнения для АРТИКУЛОВ: омоглифы в латиницу, регистр и пробелы прочь.

    Ведущие нули НЕ трогаются: у артикула каждая цифра значащая ('8000099046'
    и '800099046' - разные изделия).
    """
    if s is None:
        return ""
    return _SPACES_RE.sub("", str(s).translate(TO_LATIN).upper())


def fold_designator(s):
    """Ключ сравнения для ПОЗИЦИОННЫХ ОБОЗНАЧЕНИЙ: как fold(), плюс свёрнутые
    ведущие нули.

    Только ключ сравнения - наружу, в находку, всегда идёт обозначение в том
    виде, в каком оно написано в документе.
    """
    return _LEADING_ZERO_RE.sub("", fold(s))


def to_cyrillic(token: str) -> str:
    """Привести смешанную раскладку к кириллице - для обозначений, которые
    ЧИТАЕТ ЧЕЛОВЕК (имя папки шкафа, название связки).

    Только для токенов, где кириллица УЖЕ есть: чисто латинское обозначение
    ('PLC', 'DO1') - это не мешанина раскладок, а честная латиница, и портить
    её нельзя.
    """
    if any("А" <= ch <= "я" or ch in "Ёё" for ch in token):
        return token.translate(TO_CYRILLIC)
    return token
