# ОБОЗНАЧЕНИЕ ШКАФА из наименования/имени файла.
#
# Живёт ЗДЕСЬ, а не в full_project.py, потому что пользователей у него теперь
# ДВА: full_project.py (шкаф из наименования штампа при нарезке альбома) и
# web_app/sessions.py (шкаф из имён файлов комплекта - для автонейминга сессии).
# full_project тянет fitz, а web_app по замыслу на голой стдлибе; bundles.py -
# стдлиб и уже про то, что такое связка/шкаф, - единственное место, куда
# дотягиваются оба. Одна копия логики на оба, как у GENERATED_MARKER.
# ---------------------------------------------------------------------------

# Аббревиатура из заглавных букв, возможно с цифрой (ЩС1, ШУПЧ4, ВРУ, БУЗО,
# ПЭСПЗ, ЩРТХ, ЩСКЗ). Латиница включена - бюро мешает раскладки ("ЩC1" с
# латинской C встречается в реальных файлах).
CABINET_RE = re.compile(r"\b([А-ЯЁA-Z]{2,7}\d{0,2})\b")

# Слова, похожие на обозначение шкафа по форме, но им не являющиеся.
CABINET_STOP = {
    "СХЕМА", "ВИД", "ОБЩИЙ", "ПЛАН", "ЩИТ", "ШКАФ", "ШКАФА", "ЩИТА", "АВР",
    "КИПИА", "ГОСТ", "ПЗ", "СО", "СБ", "НЛ", "МВТ", "КВТ", "ТОМ", "ЛИСТ",
    "ДЛЯ", "НА", "И", "С", "ПО", "ОТМ", "УЗЛА", "УЧЕТА", "УЧЁТА",
    "СПЕРЕДИ", "СЗАДИ", "СБОКУ", "СЛЕВА", "СПРАВА",
}

# Приведение раскладки к кириллице: бюро мешает латиницу и кириллицу в одном
# обозначении ("ЩC1" с латинской C и "ЩС1" с кириллической - один шкаф). Таблица
# омоглифов общая (normalize.py). Здесь сворачиваем в КИРИЛЛИЦУ: обозначение
# становится именем папки/связки, которое читает человек.
_unify_layout = normalize.to_cyrillic


def detect_cabinet(title):
    """Обозначение шкафа из наименования ("...принципиальная ЩС2" -> "ЩС2").

    Шкаф - это ключ связки: в альбоме их до полутора десятков, и обозначения
    в них пересекаются (1QF1 есть и в ШУПЧ1, и в ЩС1, но это РАЗНЫЕ аппараты).
    Сверять их между собой нельзя, иначе bundle_rules выдаст вал ложных
    "разный артикул у одного обозначения".
    """
    # Аббревиатура в скобках точнее прочего: "щит автоматики котла (ЩАК1)".
    for chunk in re.findall(r"\(([^)]{2,12})\)", title):
        m = CABINET_RE.fullmatch(chunk.strip())
        if m and m.group(1).upper() not in CABINET_STOP:
            return _unify_layout(m.group(1).upper())

    # Иначе - последняя подходящая аббревиатура: наименование строится как
    # "<вид документа> <объект>", и обозначение шкафа стоит в конце.
    for m in reversed(list(CABINET_RE.finditer(title))):
        token = m.group(1).upper()
        if token in CABINET_STOP or token.isdigit():
            continue
        # Кусок составного кода объекта - не шкаф: в "ТТС-БМК-48000" средний
        # сегмент по форме неотличим от обозначения щита, но это шифр всей
        # котельной, и спецификация с таким "шкафом" уехала бы в собственную
        # связку вместо общей.
        before = title[m.start() - 1: m.start()]
        after = title[m.end(): m.end() + 1]
        if before == "-" or after == "-":
            continue
        return _unify_layout(token)
    return None


def assign_bundles(documents, overrides=None):
    """Проставляет каждому документу манифеста поле bundle.

    documents: список записей манифеста (dict с 'name', 'source_file', ...).
    overrides: {"имя файла": "имя связки"} - явное указание.

    Приоритет: overrides -> подпапка в base_files -> DEFAULT_BUNDLE.
    """
    overrides = overrides or {}

    for doc in documents:
        source = doc.get("source_file") or ""
        filename = source.rsplit("/", 1)[-1]

        bundle = overrides.get(filename)
        source_of = "форма загрузки"

        if not bundle:
            # подпапка внутри base_files: ".../base_files/связка 1/файл.pdf"
            parts = source.split("/")
            if len(parts) >= 2 and parts[-2] and parts[-2] != "base_files":
                bundle = parts[-2]
                source_of = "подпапка"

        if not bundle:
            bundle = DEFAULT_BUNDLE
            source_of = "все документы прогона - один проект"

        doc["bundle"] = bundle
        doc["bundle_source"] = source_of

    return documents


def group_by_bundle(documents):
    """{имя связки: [документы]} с сохранением порядка появления."""
    groups = OrderedDict()
    for doc in documents:
        groups.setdefault(doc.get("bundle") or DEFAULT_BUNDLE, []).append(doc)
    return groups


def bundle_summary(documents):
    """Сводка по связкам для манифеста: что в связке есть.

    Поля "чего не хватает" здесь СОЗНАТЕЛЬНО НЕТ: состав комплекта не фиксирован,
    и отсутствие спецификации или чертежа - не дефект, а выбор пользователя.
    """
    groups = group_by_bundle(documents)
    out = []
    for name, docs in groups.items():
        types = {}
        for d in docs:
            types.setdefault(d.get("doc_type"), []).append(d.get("name"))
        comparable = [t for t in BUNDLE_DOC_TYPES if t in types]
        out.append({
            "bundle": name,
            "documents": [d.get("name") for d in docs],
            "doc_types": {k: v for k, v in types.items() if k},
            # по скольким типам документов вообще возможна сверка внутри связки:
            # при одном таком типе сверять не с чем, и это нормально
            "cross_checkable_types": comparable,
            "cross_checkable": len(comparable) >= 2,
        })
    return out
