"""
СВЯЗКА документов: какие документы относятся к ОДНОМУ И ТОМУ ЖЕ проекту (шкафу).

ГЛАВНОЕ ПРАВИЛО: за один прогон анализатора загружаются документы ОДНОГО
проекта. Поэтому по умолчанию ВСЕ документы прогона - ОДНА связка. Никакого
разбиения "по догадке" не делается.

Почему так, а не "умнее". Сначала связка вычислялась из имени файла - общим
префиксом до марки вида документа ("026.809.01.01-ИПК ША1" + "Э3"/"СБ"/"СО").
На одном наборе файлов это работало, на следующем же развалилось:

    026_812_19_00CJF02_ИК_3192_АТХ2_015_СО_29_04_26_—_копия.xlsx  -> ключ "026_812_19_00CJF02_ИК_3192_АТХ2_015"
    СБ_ИК.3912-АТХ2.015_01.06.2026.pdf                            -> ключ ""  (марка стоит ПЕРЕДИ!)
    СХ_ИК.3912-АТХ2.115_05.07.2026.pdf                            -> ключ ""
    NL_ИК.3912-АТХ3.115_05.07.2026.pdf                            -> ключ ""

Один комплект одного шкафа разъехался на две "связки", и сверять стало нечего.
Причина фундаментальная: имя файла - это не данные, а привычка конкретного бюро.
Марка вида может стоять и в конце ("... ША1 СБ_08.05.26"), и в начале
("СБ_ИК.3912-..."); обозначение проекта пишется то через точки, то через
подчёркивания; в имени бывает "— копия". Угадывать по такому - значит регулярно
ошибаться в ту сторону, где сверка молча не выполняется.

СОСТАВ СВЯЗКИ НЕ ФИКСИРОВАН. Это может быть схема + сборочный чертёж +
спецификация + нетлист внешних подключений; может быть только сборочный чертёж
со спецификацией; может быть одна схема. Отсутствие документа в связке - НЕ
ошибка: пользователь грузит то, что у него есть, и требовать полный комплект
из трёх документов нельзя.

КАК ОПРЕДЕЛЯЕТСЯ СВЯЗКА (по убыванию приоритета):

1. ЯВНО - аргументом bundles={"файл.pdf": "связка 1"}.
2. ПО ПОДПАПКЕ в base_files: всё, что лежит в data/base_files/связка 1/, -
   одна связка. Это единственный способ проанализировать несколько проектов
   за один прогон: пользователь сам разложил файлы по папкам, и это уже
   не догадка, а его явное решение.
3. ИНАЧЕ - все документы прогона считаются одной связкой (DEFAULT_BUNDLE).
"""

import logging
from collections import OrderedDict

logger = logging.getLogger(__name__)

# Имя связки по умолчанию: все документы прогона - один проект.
DEFAULT_BUNDLE = "проект"

# Типы документов, которые сверяются между собой внутри связки.
# netlist сюда не входит: он сверяется со схемой отдельной парой правил
# (netlist <-> scheme), а не по позиционным обозначениям.
BUNDLE_DOC_TYPES = ("scheme", "assembly", "spec")

DOC_TYPE_RU = {
    "scheme": "принципиальная схема (Э3)",
    "assembly": "сборочный чертёж (СБ)",
    "spec": "спецификация (СО)",
    "netlist": "таблица подключений (нетлист)",
}


def assign_bundles(documents, overrides=None):
    """Проставляет каждому документу манифеста поле bundle.

    documents: список записей манифеста (dict с 'name', 'source_file', ...).
    overrides: {"имя файла": "имя связки"} - явное указание.

    Приоритет: overrides -> подпапка в base_files -> DEFAULT_BUNDLE.
    """
    overrides = overrides or {}

    for doc in documents:
        source = doc.get("source_file") or ""
        filename = source.rsplit("/", 1)[-1]

        bundle = overrides.get(filename)
        source_of = "форма загрузки"

        if not bundle:
            # подпапка внутри base_files: ".../base_files/связка 1/файл.pdf"
            parts = source.split("/")
            if len(parts) >= 2 and parts[-2] and parts[-2] != "base_files":
                bundle = parts[-2]
                source_of = "подпапка"

        if not bundle:
            bundle = DEFAULT_BUNDLE
            source_of = "все документы прогона - один проект"

        doc["bundle"] = bundle
        doc["bundle_source"] = source_of

    return documents


def group_by_bundle(documents):
    """{имя связки: [документы]} с сохранением порядка появления."""
    groups = OrderedDict()
    for doc in documents:
        groups.setdefault(doc.get("bundle") or DEFAULT_BUNDLE, []).append(doc)
    return groups


def bundle_summary(documents):
    """Сводка по связкам для манифеста: что в связке есть.

    Поля "чего не хватает" здесь СОЗНАТЕЛЬНО НЕТ: состав комплекта не фиксирован,
    и отсутствие спецификации или чертежа - не дефект, а выбор пользователя.
    """
    groups = group_by_bundle(documents)
    out = []
    for name, docs in groups.items():
        types = {}
        for d in docs:
            types.setdefault(d.get("doc_type"), []).append(d.get("name"))
        comparable = [t for t in BUNDLE_DOC_TYPES if t in types]
        out.append({
            "bundle": name,
            "documents": [d.get("name") for d in docs],
            "doc_types": {k: v for k, v in types.items() if k},
            # по скольким типам документов вообще возможна сверка внутри связки:
            # при одном таком типе сверять не с чем, и это нормально
            "cross_checkable_types": comparable,
            "cross_checkable": len(comparable) >= 2,
        })
    return out
